type t = {init_a : Int32.t; init_b : Int32.t}

let default = {init_a = 0l; init_b = 0l}

let encoding =
  let open Data_encoding in
  conv
    (fun {init_a; init_b} -> (init_a, init_b))
    (fun (init_a, init_b) -> {init_a; init_b})
    (obj2 (dft "init_a" int32 0l) (dft "init_b" int32 0l))

let from_json json = Data_encoding.Json.destruct encoding json
